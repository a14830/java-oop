class Viatutas {
    String tipo;
    String rodas;
    int velMax;

    public Viatutas() {
    }

    public Viatutas(String tipo, String rodas, int velMax) {
        this.tipo = tipo;
        this.rodas = rodas;
        this.velMax = velMax;
    }

    public String getTipo() {
        return tipo;
    }
    
}

